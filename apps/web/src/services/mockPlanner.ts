function extractGuideFragments(text: string) {
  return Array.from(
    new Set(
      text
        .split(/[\r\n、，,；;。.!！?？]/)
        .map((item) => item.trim().replace(/^[-*•\d.、\s]+/, ''))
        .map((item) => item.replace(/^(推荐|避坑|打卡|路线|上午|下午|晚上|Day\s*\d+)[:：\s]*/i, ''))
        .filter((item) => item.length >= 2 && item.length <= 24)
        .filter((item) => /[一-龥]/.test(item))
    )
  );
}

function cleanPlaceName(name: string, destination: string) {
  const trimmed = name.trim().slice(0, 24);
  return trimmed.includes(destination) || destination === '目的地' ? trimmed : `${destination}${trimmed}`;
}

function inferType(name: string, fallback: Place['type']): Place['type'] {
  if (/餐厅|饭店|火锅|小吃|米粉|早茶|海鲜|烤鸭|粉丝汤|肉夹馍|菜馆/.test(name)) return '餐厅';
  if (/夜市|夜游|不夜城|坡子街|锦里|回民街|曾厝垵/.test(name)) return '夜市';
  if (/商圈|步行街|太古里|银泰|观音桥|新街口|小寨|北京路|上下九|中山路/.test(name)) return '商圈';
  if (/博物馆|博物院|展馆|陈家祠/.test(name)) return '博物馆';
  if (/山|湖|溪|公园|森林|西湖|玄武湖|环岛路|颐和园/.test(name)) return '自然风景';
  if (/机场|车站|码头|索道|轻轨|天梯|交通/.test(name)) return '交通点';
  return fallback;
}

function warningFor(preference: TravelPreference, type: Place['type']) {
  if (preference.dislikes.includes('排队')) return '热门时段可能排队，建议错峰或保留备选。';
  if (preference.dislikes.includes('太赶路')) return '建议与相邻区域组合，避免跨城折返。';
  if (type === '自然风景' && preference.dislikes.includes('爬山')) return '如涉及爬坡，可替换为更轻松的市区景点。';
  if (preference.budgetLevel === 'low' && ['景点', '交通点'].includes(type)) return '注意门票或交通票成本，低预算可选免费替代点。';
  return '提前核对开放时间，周末适当错峰。';
}

function transportText(preference: TravelPreference) {
  const first = preference.transportPreference[0];
  return first ? `优先${first}，跨区时保留打车选项。` : '优先选择少换乘路线。';
}

function parseDate(value: string) {
  if (!value) return null;
  const date = new Date(value);
  return Number.isNaN(date.getTime()) ? null : date;
}

function addDays(date: Date, days: number) {
  const next = new Date(date);
  next.setDate(next.getDate() + days);
  return next;
}
